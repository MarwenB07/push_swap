#include "../push_swap.h"

/* main */

int	main(int argc, char **argv)
{
	int		i;
	t_swap	*swap;

	i = 0;
	swap = malloc(sizeof(t_swap));
	if (!swap)
		return (0);
	if (argc <= 1)
		return (exit_swap(swap));
	make_a(swap, argc, argv);
	sort_complete(swap);
	free(swap->na);
	free(swap->nb);
	free(swap->tab);
	free(swap->a);
	free(swap->b);
	free(swap);
	return (0);
}

