int	exit_swap4(t_swap *swap, char **tab, int choice)
{
	int	i;
	int	y;

	i = 0;
	y = swap->size;
	while (i < y)
	{
		free(tab[i]);
		i++;
	}
	free(tab);
	free(swap->a);
	free(swap->b);
	free(swap->na);
	free(swap->nb);
	free(swap->tab);
	free(swap);
	if (choice == 0)
		ft_putstr_error("Error\n");
	exit(0);
}
